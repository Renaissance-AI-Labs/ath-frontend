<script>
export default {
  name: 'AboutView',
  mounted() {
    this.loadScript('/asset/js/SplitText.min.js')
      .then(() => {
        this.initAnimation();
      })
      .catch(() => {
        console.error('Failed to load SplitText.min.js');
      });
  },
  methods: {
    loadScript(src) {
      return new Promise((resolve, reject) => {
        if (document.querySelector(`script[src="${src}"]`)) {
          return resolve();
        }
        const script = document.createElement('script');
        script.src = src;
        script.onload = resolve;
        script.onerror = reject;
        document.body.appendChild(script);
      });
    },
    initAnimation() {
      this.$nextTick(() => {
        if (typeof SplitText === 'undefined' || typeof gsap === 'undefined') {
          console.error('GSAP or SplitText is not available.');
          return;
        }

        if (document.querySelector(".text-color-change")) {
          gsap.registerPlugin(SplitText);
          let animatedTextElements = document.querySelectorAll(".text-color-change");

          animatedTextElements.forEach((element) => {
            if (element.wordSplit) {
              element.wordSplit.revert();
            }
            if (element.charSplit) {
              element.charSplit.revert();
            }

            element.wordSplit = new SplitText(element, {
              type: "words",
              wordsClass: "word-wrapper",
            });

            element.charSplit = new SplitText(element.wordSplit.words, {
              type: "chars",
              charsClass: "char-wrapper",
            });

            gsap.set(element.charSplit.chars, {
              color: "#F4F7F5",
              opacity: 1,
            });

            element.animation = gsap.to(element.charSplit.chars, {
              scrollTrigger: {
                trigger: element,
                start: "top 90%",
                end: "bottom 35%",
                toggleActions: "play none none reverse",
                scrub: true,
              },
              color: "#5997FF",
              stagger: {
                each: 0.05,
                from: "start",
              },
              duration: 0.5,
              ease: "power2.out",
            });
          });
        }
      });
    }
  }
};
</script>
